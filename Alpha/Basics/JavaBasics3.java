public class JavaBasics3 {

// Data Types
    public static void main(String args[]) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.print(ch + "\n"); 
        boolean var = false;
        System.out.println(var);
        double price = 10.5;
        System.out.println(price);
        int number = 25;
        System.out.println(number);
        // long
        // double
        short n = 240;
        System.out.println(n);
    }
}